package org.eolang.maven;

import com.jcabi.log.Logger;
import java.io.IOException;
import org.apache.maven.plugins.annotations.LifecyclePhase;
import org.apache.maven.plugins.annotations.Mojo;
import org.apache.maven.plugins.annotations.Parameter;
import org.apache.maven.plugins.annotations.ResolutionScope;

/**
 * Transpile.
 * <p>
 *     This Maven Mojo transpiles XMIR files into Java source files.
 *     The goal of this class is to automate the process of transforming EO
 *     intermediate representations (XMIRs) into Java code, optimize the output,
 *     and integrate the results into the Maven build lifecycle for further
 *     processing.
 * </p>
 * <p>
 *     This is one of the final goals that transpiles XMIRs to Java files generated by
 *     {@link MjAssemble}, {@link MjParse} or {@link MjPull} goals.
 *     The resulting Java files are stored in the {@link Transpiling#DIR} directory.
 *     The intermediate optimized XMIRs are stored in the {@link Transpiling#PRE} directory.
 * </p>
 * @since 0.1
 */
@Mojo(
    name = "transpile",
    defaultPhase = LifecyclePhase.PROCESS_SOURCES,
    threadSafe = true,
    requiresDependencyResolution = ResolutionScope.COMPILE
)
public final class MjTranspile extends MjSafe {

    /**
     * Add to source root.
     * @checkstyle MemberNameCheck (7 lines)
     */
    @Parameter(property = "eo.addSourcesRoot")
    @SuppressWarnings("PMD.ImmutableField")
    private boolean addSourcesRoot = true;

    /**
     * Whether to transpile tests.
     * @checkstyle MemberNameCheck (7 lines)
     */
    @Parameter(property = "eo.transpileTests")
    @SuppressWarnings("PMD.ImmutableField")
    private boolean transpileTests = true;

    /**
     * Whether to wrap every dispatched object with a location-carrying
     * {@code PhSafe}, so a panic reports its {@code .eo} source position.
     * Off by default, so a production build stays lean; turn it on (as
     * {@code eo-runtime} does for its tests) to keep precise {@code .eo}
     * locations in panics.
     * @checkstyle MemberNameCheck (7 lines)
     */
    @Parameter(property = "eo.trackLocations")
    private boolean trackLocations;

    @Override
    public void exec() throws IOException {
        new Timed(
            new Transpiling(
                this.scopedTojos().withXmir(),
                this.targetDir.toPath(),
                this.generatedDir.toPath(),
                this.cache.toPath(),
                this.cacheEnabled,
                this.plugin.getVersion(),
                this.transpileTests,
                this.xslMeasures.toPath(),
                new Tracking(this.trackTransformationSteps, this.trackLocations)
            )
        ).exec();
        if (this.addSourcesRoot) {
            this.project.addCompileSourceRoot(
                this.generatedDir.toPath().toAbsolutePath().toString()
            );
            Logger.info(
                this, "The directory added to Maven 'compile-source-root': %[file]s",
                this.generatedDir
            );
            final String gtests = this.generatedDir.toPath().getParent().resolve(
                "generated-test-sources"
            ).toAbsolutePath().toString();
            this.project.addTestCompileSourceRoot(gtests);
            Logger.info(
                this, "The directory added to Maven 'test-compile-source-root': %[file]s",
                gtests
            );
        }
    }
}
